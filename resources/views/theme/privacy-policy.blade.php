    إذا تبين أن هذه الفقرة المعينة “غير قابلة للتنفيذ” القرار “سيكون باطلاً. باستثناء ما هو منصوص عليه في الجملة السابقة، يبقى القسم المعنون “حل النزاعات” ساريًا بعد إنهاء هذه الاتفاقية. 
    ينطبق شرط “حل النزاع” هذا على جميع النزاعات (كما هو موضح أدناه) بينك وبين اوكي و / أو الشركات التابعة لها.
</p>

<h3>13.2 إجراءات وقواعد التحكيم</h3>
<p>
    أي نزاع أو خلاف أو مطالبة أو صراع ينشأ عن أو يرتبط بشكل جوهري بمنصة اوكي أو ينشأ عن هذه الاتفاقية أو يتعلق بها (بما في ذلك ما يتعلق بصلاحية هذه الاتفاقية أو إنشائها أو قااوكيتها للتنفيذ) (“النزاعات”); راجع 1 إجراءات الوساطة بموجب قواعد الوساطة للمحكمة الجنائية الدولية (“قواعد الوساطة للمحكمة الجنائية الدولية”). 
    إذا لم يتم حل هذا النزاع في غضون ستين (60) يومًا بعد تقديم طلب الوساطة بموجب قواعد الوساطة الخاصة بغرفة التجارة الدولية، فسيكون هذا النزاع بمثابة قواعد التحكيم “. داخل غرفة التجارة الدولية). 
    شرط” محكم الطوارئ ” تم استبعاده أيضًا من قواعد التحكيم الخاصة بغرفة التجارة الدولية. 
    يتم حل النزاعات بواسطة محكم واحد يتم تعيينه وفقًا لقواعد غرفة التجارة الدولية. يكون مكان الوساطة والتحكيم هو مدينة المركز الرئيسي للطرف الآخر في النزاع. 
    يجب أن تكون لغة الوساطة و / أو التحكيم هي اللغة الإنجليزية، إلا إذا كنت لا تتحدث الإنجليزية. 
    في مثل هذه الحالة، يجب إجراء الوساطة و / أو إجراءات التحكيم باللغتين الإنجليزية واللغة الأم. 
    وجود إجراءات الوساطة والتحكيم ومضمونها. ويشمل ذلك المستندات والمذكرات المقدمة من الأطراف، والمراسلات مع غرفة التجارة الدولية، والمراسلات الصادرة عن المحكم، والمراسلات الصادرة عن المحكم الوحيد، والأوامر والجوائز. 
    – يجب أن تظل سرية ولا يجوز الكشف عنها لأي طرف ثالث دون موافقة كتابية صريحة من الطرف الآخر باستثناء: أو إجراءات التحكيم. (2) يوافق الطرف الثالث كتابةً دون قيد أو شرط على الالتزام بالتزامات السرية المنصوص عليها في هذه الاتفاقية.
</p>

<h2>14. أحكام أخرى</h2>

<h3>14.1 اختيار القانون المنظم</h3>
<p>
    هذه الاتفاقية تخضع وتفسر وفقاً لقوانين السلطة القضائية التي تكون الشركة التابعة ذات الصلة مؤسسة فيها، من دون ان يتم تطبيق اي مبادئ لا تتماشى مع القانون(تضارب قانون)، باستثناء ما منصوص عليه.
</p>

<h3>14.2 دعاوى انتهاك حقوق النشر</h3>
<p>
    أي انتهاك في حقوق الطبع والنشر يجب ارسال المطالبات الخاصة الى شركة اوكي على عنوان البريد الالكتروني iraq24okay24@gmail.com
</p>

<h3>14.3 الإشعارات</h3>
<p>
    يسمح لمنصة بَلي أرسال الاشعارات العامة، أو أرسال الرسائل من خلال منصات التواصل الاجتماعي أو من خلال البريد الالكتروني الخاص بمنصة بَلي الى عنوان بريدك الالكتروني مباشرةً او من خلال المكالمات أو من خلال ارسال خطاب مباشر الى عنوانك. 
    ويمكنك ارسال او الاتصال بمنصة بَلي حسب الوسائل التي توفرها شركة بَلي.
</p>

<h3>14.4 الأحكام العامة</h3>
<p>
    لا يسمح للمستخدم التنازل عن حقوقه أو نقلها للغير وبحسب هذه الأتفاقية (كلياً او جزئياً) من دون موافقة خطية مسبقة من شركة اوكي. 
    ويجب عليك ان تكون ملزم بالموافقة على أن شركة بَلي يحق لها التنازل عن حقوقها والتزاماتها أو نقلها بحسب هذه الأتفاقية (كلياً او جزئياً) الى الجهات التالية: 
    (1) شركات فرعية أو تابعة (2) أي جهة تستحوذ على حقوق الملكية لشركة بَلي أو أعمال أو أصول شركة بَلي. 
    كذلك (3)أي شخص يكون خليفة عن طريق الدمج ولا يقود هذا العقد الى إنشاء أي شراكة او طلب توظيف أو شراكة أو وكالة او مشروع بينك وبين بَلي أو أي (موظف- سائق).
</p>
<p>
    يجب على الاطراف المعنية استبدال البنود او احكام هذه الاتفاقية اذا تم اعتبار أي بند من بنود هذه الاتفاقية غير صالح قانونياً او غير قابل للتطبيق، دون ان تتأثر قانونية وقااوكية تنفيذ الاحكام الاخرى المذكورة والواردة في الاتفاقية.
</p>

<h3 dir="rtl"><span style="font-size: 19.72px;"><strong>15. البيانات التي نجمعها</strong></span></h3>
<p dir="rtl"><strong>المعلومات الشخصية</strong>: مثل الاسم، رقم الهاتف، وعنوان البريد الإلكتروني لتسجيل الحساب والتواصل.</p>
<p dir="rtl"><strong>بيانات الموقع الجغرافي</strong>: لتحديد موقع المستخدم وتقديم خدمات النقل بدقة.</p>
<p dir="rtl"><strong>معلومات الجهاز</strong>: مثل نوع الجهاز، نظام التشغيل، ومعرّف الجهاز لتحسين الأداء.</p>
<p dir="rtl"><strong>بيانات الدفع</strong>: عند إجراء المدفوعات عبر التطبيق، يتم استخدام بوابات دفع آمنة لضمان سرية معلوماتك المالية.</p>
<h3 dir="rtl"><span style="font-size: 19.72px;"><strong>15.1 كيفية استخدام البيانات</strong></span></h3>
<p dir="rtl">نستخدم بياناتك للأغراض التالية:</p>
<p dir="rtl">تقديم خدمات النقل والتوصيل.</p>
<p dir="rtl">تحسين تجربة المستخدم وتطوير الخدمات.</p>
<p dir="rtl">التواصل معك بشأن الطلبات أو المشكلات.</p>
<p dir="rtl">تحليل البيانات لتحسين الأداء واتخاذ القرارات الاستراتيجية.</p>
<h3 dir="rtl"><strong><span style="font-size: 19.72px;">15.2 مشاركة البيانات</span></strong></h3>
<p dir="rtl">نحن ملتزمون بحماية خصوصيتك، ولا نشارك بياناتك إلا في الحالات التالية:</p>
<ul dir="rtl">
  <li>مع مزودي الخدمات الخارجيين (مثل خدمات الخرائط) لضمان تقديم الخدمات.</li>
  <li>للامتثال للالتزامات القانونية أو طلبات الجهات المختصة.</li>
  <li>بموافقتك الصريحة على مشاركة بياناتك.</li>
</ul>
<h3 dir="rtl"><strong><span style="font-size: 19.72px;">15.3 حماية البيانات</span></strong></h3>
<p dir="rtl">نستخدم تقنيات وإجراءات أمان متقدمة لحماية بياناتك من الوصول غير المصرح به أو الاستخدام غير القانوني، بما في ذلك:</p>
<p dir="rtl">استخدام بروتوكولات تشفير متقدمة.</p>
<p dir="rtl">مراجعات دورية لأنظمة الأمان.</p>
<p dir="rtl">تقييد الوصول إلى المعلومات الشخصية.</p>
<h3 dir="rtl"><strong><span style="font-size: 19.72px;">15.4 مدة الاحتفاظ بالبيانات</span></strong></h3>
<p dir="rtl">نحتفظ ببياناتك فقط للمدة التي تكون فيها هناك حاجة قانونية أو تشغيلية لها. وبعد انتهاء الغرض من جمع البيانات، يتم حذفها بطريقة آمنة.</p>
<h3 dir="rtl"><strong><span style="font-size: 19.72px;">15.5 حقوقك</span></strong></h3>
<p dir="rtl">لديك حقوق متعددة فيما يتعلق ببياناتك، بما في ذلك:</p>
<p dir="rtl">الوصول إلى معلوماتك الشخصية.</p>
<p dir="rtl">طلب تعديل أو تصحيح المعلومات.</p>
<p dir="rtl">طلب حذف معلوماتك الشخصية.</p>
<p dir="rtl">الاعتراض على معالجة البيانات في ظروف معينة.</p>
